// Cyrillic letters that reuse Latin shapes.
const CYR_ALIAS = { 'А': 'A', 'В': 'B', 'Е': 'E', 'Ё': 'E', 'К': 'K', 'М': 'M', 'Н': 'H', 'О': 'O', 'Р': 'P', 'С': 'C', 'Т': 'T', 'Х': 'X' };

const LINE_H = 9;
const SPACE_W = 4;

function glyphData(ch) {
  const up = ch.toUpperCase();
  return G[up] || G[CYR_ALIAS[up]] || null;
}

function glyphW(ch) {
  const g = glyphData(ch);
  return g ? g[0].length : SPACE_W - 1;
}

// color -> Map(char -> canvas)
const atlases = new Map();

function tintedGlyph(ch, color) {
  let m = atlases.get(color);
  if (!m) { m = new Map(); atlases.set(color, m); }
  let c = m.get(ch);
  if (c !== undefined) return c;
  const g = glyphData(ch);
  if (!g) { m.set(ch, null); return null; }
  const w = g[0].length;
  c = makeCanvas(w, 7);
  const x = c.getContext('2d');
  x.fillStyle = color;
  for (let r = 0; r < 7; r++) {
    const row = g[r];
    for (let i = 0; i < w; i++) if (row[i] === '#') x.fillRect(i, r, 1, 1);
  }
  m.set(ch, c);
  return c;
}

export function textWidth(str, scale = 1) {
  let w = 0;
  for (const ch of str) w += (ch === ' ' ? SPACE_W : glyphW(ch) + 1);
  return Math.max(0, w - 1) * scale;
}

// Draw a single line. align: 'left' | 'center' | 'right'
export function drawText(ctx, str, x, y, color = '#fff', { align = 'left', scale = 1, shadow = null } = {}) {
  str = String(str);
  if (align !== 'left') {
    const w = textWidth(str, scale);
    x -= align === 'center' ? Math.floor(w / 2) : w;
  }
  if (shadow) drawText(ctx, str, x + scale, y + scale, shadow, { align: 'left', scale });
  let cx = x;
  for (const ch of str) {
    if (ch === ' ') { cx += SPACE_W * scale; continue; }
    const g = tintedGlyph(ch, color);
    if (g) {
      if (scale === 1) ctx.drawImage(g, cx, y);
      else ctx.drawImage(g, cx, y, g.width * scale, 7 * scale);
      cx += (g.width + 1) * scale;
    } else {
      cx += SPACE_W * scale;
    }
  }
  return cx;
}

// Word-wrapped multi-line text. Returns total height.
export function drawTextWrapped(ctx, str, x, y, maxW, color = '#fff', opts = {}) {
  const scale = opts.scale || 1;
  const lines = [];
  for (const para of String(str).split('\n')) {
    let line = '';
    for (const word of para.split(' ')) {
      const test = line ? line + ' ' + word : word;
      if (textWidth(test, scale) > maxW && line) { lines.push(line); line = word; }
      else line = test;
    }
    lines.push(line);
  }
  lines.forEach((ln, i) => drawText(ctx, ln, x, y + i * LINE_H * scale, color, opts));
  return lines.length * LINE_H * scale;
}

