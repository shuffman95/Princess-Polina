// All level maps — 100% original layouts, authored as horizontal segments.
// See levels.js for the tile/entity legend. Rows are 15 tiles tall (240px).
//
// Ability pacing (unlocks come from freeing each world's guardian):
//   W1-2: run/jump only    W3: +walljump,climb   W4: +pound   W5: +roll
//   W6: +swim              W7: +doublejump       W8: +dash    W9: +glide
// Secrets may reward returning with later abilities.

const H = 15;
const dots = (w) => '.'.repeat(w);

// A segment: w columns, `ground` solid rows at the bottom, then apply marks [row, col, str].
function seg(w, ground = 2, marks = []) {
  const rows = [];
  for (let r = 0; r < H; r++) {
    rows.push(r >= H - ground ? '#'.repeat(w) : dots(w));
  }
  for (const [r, c, s] of marks) {
    rows[r] = rows[r].slice(0, c) + s + rows[r].slice(c + s.length);
  }
  return rows;
}

// Bottomless pit (or hazard floor via marks).
const pit = (w, marks = []) => seg(w, 0, marks);

// Lava pool: glowing surface at row 12, lava below.
function lava(w, marks = []) {
  return seg(w, 0, [[12, 0, '%'.repeat(w)], [13, 0, 'l'.repeat(w)], [14, 0, 'l'.repeat(w)], ...marks]);
}

// Water pool with solid floor: surface at `surf` row.
function pool(w, surf = 10, marks = []) {
  const m = [[surf, 0, '~'.repeat(w)]];
  for (let r = surf + 1; r < H - 1; r++) m.push([r, 0, 'w'.repeat(w)]);
  return seg(w, 1, [...m, ...marks]);
}

// Vertical stroke helper (used via marks arrays below by pre-building strings).
function col(marks, r0, r1, c, ch) {
  for (let r = r0; r <= r1; r++) marks.push([r, c, ch]);
  return marks;
}

// Join segments horizontally.
function J(...blocks) {
  return Array.from({ length: H }, (_, r) => blocks.map((b) => b[r]).join(''));
}

// Standard boss arena.
function arena(extras = [], w = 36) {
  const marks = [];
  col(marks, 5, 12, 0, '='); col(marks, 5, 12, 1, '=');
  col(marks, 5, 12, w - 1, '='); col(marks, 5, 12, w - 2, '=');
  marks.push([12, 4, 'S'], [12, Math.floor(w * 0.66), 'G']);
  return seg(w, 2, [...marks, ...extras]);
}

